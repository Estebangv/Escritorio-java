    private void tab_unidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab_unidadMouseClicked
        clic_tabla = this.tab_unidad.rowAtPoint(evt.getPoint());

        String id_unidad = "" + tab_unidad.getValueAt(clic_tabla, 0);
        String nombre = "" + tab_unidad.getValueAt(clic_tabla, 1);
        String descripcion = "" + tab_unidad.getValueAt(clic_tabla, 2);

       
        txtId.setText(id_unidad);
        txtNombre.setText(nombre);
        txtDescripción.setText(descripcion);

        int column = tab_unidad.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tab_unidad.getRowHeight();

        if (row < tab_unidad.getRowCount() && row >= 0 && column < tab_unidad.getColumnCount() && column >= 0) {
            Object value = tab_unidad.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;

                if (boton.getName().equals("mod")) {
                    System.out.println("Click en el boton modificar");
                    activa_boton(true, false, false);
                }
                if (boton.getName().equals("eli")) {
                    System.out.println("Click en el boton eliminar");
                    activa_boton(false, true, false);
                }
            }

        }
    }//GEN-LAST:event_tab_unidadMouseClicked

