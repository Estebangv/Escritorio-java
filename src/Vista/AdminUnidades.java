    public void modificar() {
        int errores = 0;
        if (txtId.getText().equals("") || txtNombre.getText().equals("") || txtDescripción.getText().equals("")) {
            errores = errores + 1;
        }

        if (errores >= 1) {
            JOptionPane.showMessageDialog(null, "Falta información, rellene los campos", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            crud_un = new CRUDUnidad();
            Unidad un = new Unidad();

            un.setId_unidad(Integer.parseInt(txtId.getText()));
            un.setNombre(txtNombre.getText());
            un.setDescripcion(txtDescripción.getText());

            crud_un.Modificar_Unidad(un);
        }

    }

    public void eliminar() {
        crud_un = new CRUDUnidad();
        Unidad un = new Unidad();

        un.setId_unidad(Integer.parseInt(txtId.getText()));
        crud_un.Eliminar_Unidad(un);
    }

